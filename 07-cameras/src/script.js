import * as THREE from "three";
import { OrbitControls } from "three/addons/controls/OrbitControls.js";

/* NOTES
- Camera: an abstract class, you're not supposed to use it directly
- Array Camera: renders the scene from multiple cameras on specific areas of the render (like a multiplayer game on the same screen split)
- Stereo Camera: renders the scene through two cameras that mimic the eyes to create a parallax effect (like VR headset, red and blue glasses, etc)
- Cube Camera: 6 renders, each one facing a different direction, can render the surronding for things like environment map, reflection, or shadow map
- Orthographic Camera: render the scene without perspective
- Perspective Camera: render the scene with perspective
 */

